from pyspark.sql import SparkSession
from pyspark.sql.functions import col, expr, mean, corr

spark = SparkSession.builder.appName("libreria").getOrCreate()

spark

data= [("tijera",40),("cuadernos ",50), ("regla",30),("goma ",20), ("borrador",25),("lapiz ",35), ("tajador",15),("plumon ",45),]
colums = ["producto","cantidad"]
variable = spark.createDataFrame(data=data,schema=colums)

var_moda = variable.groupBy("cantidad").count().orderBy(col("count").desc()).first()

print("La moda es:",var_moda["cantidad"])